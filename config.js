exports.unique = function(arr){
  var ret=[];
  var hash={};
  for(var i=0;i<arr.length;i++){
    var item=arr[i];
    var s = item.url.match(/v\=([a-zA-Z0-9_-]+)/);
    var key = typeof(item)+s[1];
    if(hash[key]!==1){
      ret.push(item);
      hash[key] =1;
    }
  }
  return ret;
};

